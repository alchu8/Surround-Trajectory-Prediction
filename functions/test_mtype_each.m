function LL = test_mtype_each(data, hmm_model_type)
% hmm_model_type = hmm_models{type, 1}
    LL = hmm_model_type;
    for cluster = 1:size(hmm_model_type, 1)
        if numel(hmm_model_type{cluster, 1}) == 0
            continue;
        end
        LL{cluster, 1} = mhmm_logprob(data, ...
            hmm_model_type{cluster, 1}.prior, ...
            hmm_model_type{cluster, 1}.transmat,...
            hmm_model_type{cluster, 1}.mu,...
            hmm_model_type{cluster, 1}.sigma,...
            hmm_model_type{cluster, 1}.mixmat);
    end
end